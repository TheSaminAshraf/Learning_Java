// Compilation: Catching Errors
// Compilation: Creating Executables

// Java is a compiled programming language.
// Terminal command for compilation:
// javac FileName.java
// Note: file names provided in the terminal command are case-sensitive.
// A successful compilation produces a .class file: FileName.class, which is executed using the terminal command:
// java FileName
// An unsuccessful compilation produces a list of errors.
// No .class file is made until all the errors are corrected and the compile command is run again.
// Using the terminal command "ls" (short for "list") lists all the files in the directory.
package FreeCourse_LearnJava;
public class Compiling {
    public static void main(String[] args) {
        System.out.println("Java is a class-based language.");
        System.out.println("Java classes have a 'main' method.");
        System.out.println("Java statements end with a semicolon.");
        System.out.println("Programming is... fun!");
    }
}