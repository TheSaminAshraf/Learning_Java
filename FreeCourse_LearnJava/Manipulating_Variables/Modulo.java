// Modulo

// The modulo operator (%) gives us the remainder after two numbers are divided.
// The modulo operator works on doubles as well.
package FreeCourse_LearnJava.Manipulating_Variables;
public class Modulo {
	public static void main(String[] args) {   
		int students = 26;
    	int leftOut = students % 3;
    	System.out.println(leftOut);
	}
}