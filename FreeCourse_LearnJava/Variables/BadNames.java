// Naming

// Naming variables according to convention leads to clear, readable and maintainable code.
// In Java, variables names are case-sensitive.
// The length of a variable name is unlimited, but we should keep it concise while keeping the meaning clear.
// A variable name starts with a valid letter, or a $, or an _.
// Variable names of only one word are spelled in all lowercase letters.
// Variable names of more than one word follow the camelCase (for example, personName) capitalization method.
package FreeCourse_LearnJava.Variables;
public class BadNames {
	public static void main(String[] args) {   
	    String firstName = "Samira";
        String blah = "Smith";
        String email = "samira@google.com";
        int salaryexpectation = 100000;
        int year_of_birth = 1955;
        System.out.println("The program runs!");
	}
}